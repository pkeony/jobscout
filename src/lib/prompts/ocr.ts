export const OCR_SYSTEM_PROMPT = `당신은 한국어 채용공고 이미지에서 텍스트를 추출하는 OCR 엔진입니다.

## 원칙
- 이미지에 쓰여있는 **모든 텍스트를 원문 그대로** 추출합니다.
- 요약하지 마세요. 재구성하지 마세요. 설명을 덧붙이지 마세요.
- 섹션 헤더, 불릿(•, -, 숫자), 표, 순서를 그대로 유지합니다.
- 여러 이미지가 주어지면 이미지 순서대로 이어서 출력합니다.

## 출력 형식
- 원문 텍스트만 출력. 마크다운 코드블록(\`\`\`) 금지. 설명 금지.
- 이미지에 없는 내용을 추가하지 마세요.`;

export function buildOcrInstruction(imageCount: number): string {
  if (imageCount === 1) {
    return "첨부된 채용공고 이미지의 텍스트를 원문 그대로 추출해주세요.";
  }
  return `첨부된 ${imageCount}개의 채용공고 이미지의 텍스트를 이미지 순서대로 원문 그대로 추출해주세요.`;
}
